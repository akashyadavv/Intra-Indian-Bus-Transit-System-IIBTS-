USE BUS_RESERVATION;
DELIMITER $$
CREATE TRIGGER updcheck1 BEFORE UPDATE ON FARE
FOR EACH ROW 
BEGIN 
IF NEW.TAX>100 THEN 
SET NEW.TAX = NEW.TAX;
ELSE 
SET NEW.TAX = 100; 
END IF; 
END;
$$


